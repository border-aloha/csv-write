create table if not exists curry_user(
	id int(10) AUTO_INCREMENT PRIMARY KEY NOT NULL,
	name varchar(255), 
	kana varchar(255), 
	address varchar(255), 
	gender varchar(255), 
	age varchar(255), 
	birthday varchar(255), 
	married varchar(255), 
	prefecture varchar(255), 
	mobile varchar(255), 
	carrier varchar(255), 
	curry varchar(255)
);
